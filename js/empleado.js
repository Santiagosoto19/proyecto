class Empleado {

    constructor(cedula, nombre) {

        this.cedula = cedula;
        this.nombre = nombre;

    }

    setCedula(nombre) {

        this.cedula = cedula;
    }

    setNombre(nombre) {

        this.nombre = nombre;
    }

    setNombre(nombre) {

        this.nombre = nombre;
    }

    getNombre() {

        return this.nombre;

    }

    getCedula() {

        return this.nombre;
        
    }
}